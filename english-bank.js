// Grade an English answer
window.gradeEnglishAnswer = function(answer, raw) {
  const text = String(raw || "").trim().toLowerCase();
  if (!text) return false;

  if (answer.type === "text") {
    // For English, check if most keywords are present
    const matched = answer.keywords.filter(kw => {
      const keyword = String(kw).toLowerCase().trim();
      if (!keyword) return false;
      if (/^[a-z]+$/.test(keyword)) {
        return new RegExp(`\\b${keyword.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\b`, "i").test(text);
      }
      return text.includes(keyword);
    });
    return matched.length >= Math.max(1, Math.floor(answer.keywords.length * 0.5));
  }

  // Number grading
  const value = Number(text.replace(/[^0-9./-]/g, ""));
  if (text.includes("/") && !text.includes(".")) {
    const [a, b] = text.split("/").map(Number);
    return b && Math.abs(a / b - answer.value) <= (answer.tolerance || 0.01);
  }
  return Number.isFinite(value) && Math.abs(value - answer.value) <= (answer.tolerance || 0.01);
};

